package com.aqsoft.myrxjavaapp.nLevel;

public class SomeObject {
    public String name;
    public int icon;

    public SomeObject(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public int getIcon() {
        return icon;
    }
}